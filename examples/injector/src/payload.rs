/// C++ StubData field offsets from the marker position.
/// These match the C++ `Payload::StubData` struct layout on aarch64:
///
/// ```text
/// +0x00: mark[16]            "/alone12345678\0\0"
/// +0x10: original_func       (8 bytes, function pointer)
/// +0x18: slot_addr            (8 bytes, ArtMethod entry_point address)
/// +0x20: uid                  (4 bytes, target UID)
/// +0x24: so_path[128]         (128 bytes, NUL-terminated path)
/// +0xA4: <padding>            (4 bytes, alignment for next u64)
/// +0xA8: fn_getuid            (8 bytes, getuid function pointer)
/// +0xB0: fn_dlopen            (8 bytes, dlopen function pointer)
/// +0xB8: fn_log               (8 bytes, __android_log_print pointer)
/// ```
mod offsets {
    pub const ORIGINAL_FUNC: usize = 0x10;
    pub const SLOT_ADDR: usize = 0x18;
    pub const UID: usize = 0x20;
    pub const SO_PATH: usize = 0x24;
    pub const SO_PATH_LEN: usize = 128;
    pub const FN_GETUID: usize = 0xA8;
    pub const FN_DLOPEN: usize = 0xB0;
    pub const FN_LOG: usize = 0xB8;
    /// Total struct size (must fit within the data region after the marker).
    pub const STRUCT_SIZE: usize = 0xC0;
}

/// Parameters for filling the stub data section of the payload.
pub struct StubParams {
    pub original_func: usize,
    pub slot_addr: usize,
    pub uid: u32,
    pub so_path: String,
    pub fn_getuid: usize,
    pub fn_dlopen: usize,
    pub fn_log: usize,
}

/// Copy the payload into a mutable buffer and fill in the StubData fields.
/// Uses explicit byte offsets matching the C++ struct layout — no struct cast.
pub fn fill_payload(params: &StubParams) -> Result<Vec<u8>, &'static str> {
    let mut buf = PAYLOAD.to_vec();

    let m = find_marker(&buf).ok_or("marker not found in payload")?;

    if m + offsets::STRUCT_SIZE > buf.len() {
        return Err("payload too small for StubData");
    }

    // Write fields at fixed offsets (little-endian, matching aarch64)
    write_u64(&mut buf, m + offsets::ORIGINAL_FUNC, params.original_func as u64);
    write_u64(&mut buf, m + offsets::SLOT_ADDR, params.slot_addr as u64);
    write_u32(&mut buf, m + offsets::UID, params.uid);

    // Write SO path (NUL-terminated)
    let path_bytes = params.so_path.as_bytes();
    let max_len = offsets::SO_PATH_LEN - 1;
    let copy_len = path_bytes.len().min(max_len);
    let dst = m + offsets::SO_PATH;
    buf[dst..dst + copy_len].copy_from_slice(&path_bytes[..copy_len]);
    buf[dst + copy_len] = 0;

    write_u64(&mut buf, m + offsets::FN_GETUID, params.fn_getuid as u64);
    write_u64(&mut buf, m + offsets::FN_DLOPEN, params.fn_dlopen as u64);
    write_u64(&mut buf, m + offsets::FN_LOG, params.fn_log as u64);

    Ok(buf)
}

fn write_u64(buf: &mut [u8], offset: usize, value: u64) {
    buf[offset..offset + 8].copy_from_slice(&value.to_le_bytes());
}

fn write_u32(buf: &mut [u8], offset: usize, value: u32) {
    buf[offset..offset + 4].copy_from_slice(&value.to_le_bytes());
}

/// Find the marker position in a byte buffer.
fn find_marker(buf: &[u8]) -> Option<usize> {
    buf.windows(MARKER.len())
        .position(|w| w == MARKER)
}
